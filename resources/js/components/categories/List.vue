<template>
    <div class="box">

                    <div class="box-header with-border">

                        <router-link :to="{ path: '/categories/create'}" class="btn btn-primary ladda-button">
                            <span class="ladda-label">
                                <i class="fa fa-plus"></i> Создать категорию
                            </span>
                        </router-link>

                        <router-link :to="{ path: '/categories/reorder'}" class="btn btn-default ladda-button">
                            <span class="ladda-label">
                                <i class="fa fa-arrows"></i> Переупорядочить категории
                            </span>
                        </router-link>

                        <input id="filter-search" type="search" class="form-control input-sm pull-right" placeholder="Поиск" v-model="filter.search">
                    </div>


                   <table class="table table-bordered ">
                            <thead>
                                <tr>
                                    <th>ID</th>
                                    <th>Родитель</th>
                                    <th>Название</th>
                                    <th>Slug</th>
                                    <th>Действия</th>
                                </tr>
                            </thead>
                            <tbody>
                                <tr class="odd even" v-for="(item, index) in categories.data">
                                    <td>{{ item.id }}</td>
                                    <td>{{ item.parent ? item.parent.name : '' }}</td>
                                    <td>{{ item.name }}</td>
                                    <td>{{ item.slug }}</td>
                                    <td>
                                        <router-link :to="{ path: '/categories/edit/' + item.id}" class="btn btn-xs btn-default">
                                            <i class="fa fa-edit"></i> Изменить
                                        </router-link>

                                        <a class="btn btn-xs btn-default" @click="deleteCategory(item, index)">
                                            <i class="fa fa-remove"></i> Удалить
                                        </a>
                                    </td>
                                </tr>
                            </tbody>
                            <tfoot>
                                <tr>
                                    <th>ID</th>
                                    <th>Родитель</th>
                                    <th>Название</th>
                                    <th>Slug</th>
                                    <th>Действия</th>
                                </tr>
                            </tfoot>
                   </table>

        <div class="text-center">
            <paginate
                    v-if="categories.last_page > 1"
                    v-model="categories.current_page"
                    :page-count="categories.last_page"
                    :click-handler="SetPage"
                    :prev-text="'<<'"
                    :next-text="'>>'"
                    :container-class="'pagination'"
                    :page-class="'page-item'"></paginate>
        </div>

        {{ categoriesList }}

    </div>
</template>


<script>
    import Paginate from 'vuejs-paginate';

    export default {
        components:{
            Paginate
        },
        data () {
            return {
                categories: [],
                filter:{
                    page:   (this.$route.query.page   ? this.$route.query.page : 1),
                    search: (this.$route.query.search ? this.$route.query.search : '')
                }
            }
        },
        mounted() {
            console.log('Component mounted.')
        },
        methods:{
            SetPage(page){
                this.filter.page = page;
            },
            deleteCategory(item, index){
                this.$swal({
                    title: 'Вы действительно хотите удалить' + ' "' + item.name + '"?',
                    type: 'warning',
                    showCancelButton: true,
                    confirmButtonColor: '#3085d6',
                    cancelButtonColor: '#d33',
                    confirmButtonText: 'Да',
                    cancelButtonText: 'Нет'
                }).then((result) => {
                    if (result.value) {
                        axios.post('/admin/category-delete/' + item.id).then((res)=>{
                            if(res.data)
                            {
                                this.$helper.swalSuccess('Успешно удален');
                                this.$delete(this.categories.data, index);
                            }
                        });
                    }
                });
            },

        },
        computed:{
            categoriesList(){
                var params = {};
                if(this.filter.page > 1 && !this.filter.search)
                    params['page'] = this.filter.page;

                if(this.filter.search)
                    params['search'] = this.filter.search;

                axios.get('/admin/categories-list', {params:  params}).then((res)=>{
                    this.categories = res.data;
                    this.$router.push({query: params});
                });
            }
        }

    }
</script>


<style scoped>
    #filter-search{
        max-width: 300px;
    }
</style>
